package com.youtube.sorcjc.redemnorte.model;

public class Hoja {

    private String id;
    private String fecha;
    private String llocal;
    private String ubicacion;
    private String responsable;
    private String cargo;
    private String dependencia;
    private String ambiente;
    private String area;
    private String inventariador;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getLlocal() {
        return llocal;
    }

    public void setLlocal(String llocal) {
        this.llocal = llocal;
    }

    public String getUbicacion() {
        return ubicacion;
    }

    public void setUbicacion(String ubicacion) {
        this.ubicacion = ubicacion;
    }

    public String getResponsable() {
        return responsable;
    }

    public void setResponsable(String responsable) {
        this.responsable = responsable;
    }

    public String getCargo() {
        return cargo;
    }

    public void setCargo(String cargo) {
        this.cargo = cargo;
    }

    public String getDependencia() {
        return dependencia;
    }

    public void setDependencia(String dependencia) {
        this.dependencia = dependencia;
    }

    public String getAmbiente() {
        return ambiente;
    }

    public void setAmbiente(String ambiente) {
        this.ambiente = ambiente;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getInventariador() {
        return inventariador;
    }

    public void setInventariador(String inventariador) {
        this.inventariador = inventariador;
    }
}
